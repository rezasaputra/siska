            <ul style="list-style-type: disc;">
                <li>&nbsp;<strong>Tugas dan Fungsi</strong></li>
            </ul>
            <p>Mengacu Peraturan Presiden Nomor 57 Tahun 2013 tentang Lembaga Administrasi Negara, Pusat Kajian Sistem dan Hukum Administrasi Negara mempunyai tugas : &ldquo;melaksanakan penyusunan rencana, penelaahan kebijakan, pengkajian dan evaluasi pelaksanaan program kajian sistem dan hukum administrasi negara, bimbingan teknis dan fasilitasi di bidang pengkajian kebijakan serta pemberian bantuan teknis dan administratif kepada pusat dan kelompok jabatan fungsional di lingkungannya&rdquo;.</p>
            <p>Dalam rangka pelaksanaan tugas tersebut, fungsi yang dijalankan oleh Pusat Kajian Sistem dan Hukum Administrasi Negara adalah</p>
            <ol>
                <li>Penyusunan rencana kerja program, kegiatan dan anggaran di bidang kajian kebijakan sistem dan hukum administrasi negara;</li>
                <li>Pelaksanaan kajian kebijakan di bidang sistem dan hukum administrasi negara;</li>
                <li>Pelaksanaan koordinasi kegiatan di lingkungan Pusat;</li>
                <li>Pengendalian pelaksanaan kegiatan di lingkungan Pusat;</li>
                <li>Pelaksanaan evaluasi kajian kebijakan di bidang sistem dan hukum administrasi negara;</li>
                <li>Pelaksanaan konsultasi, advokasi dan asistensi di bidang kajian kebijakan sistem dan hukum administrasi negara;</li>
                <li>Penyusunan dan pengembangan sistem informasi;</li>
                <li>Pelaksanaan pemberian dukungan teknis dan administratif kepada Pusat dan kelompok jabatan fungsional di lingkungan Pusat;</li>
                <li>Pembinaan kelompok jabatan fungsional di lingkungan Pusat;</li>
                <li>Pelaksanaan tugas kedinasan lain terkait yang diberikan pimpinan.</li>
            </ol>
